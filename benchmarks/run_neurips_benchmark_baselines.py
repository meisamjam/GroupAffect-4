def b1_affective_regression(pt: pd.DataFrame,
                             feature_override: list[str] | None = None) -> pd.DataFrame:
    """B1: Valence + arousal binary classification (fold-local median split).

    Binary targets remove absolute-scale sensitivity; the T2 valence dip
    creates a learnable low/high separation for valence.
    """
    features = feature_override if feature_override is not None else available_columns(pt, PHYSIO_PUPIL_DELTA + AUDIO_ABS_FEATURES)
    feat_label = "preprocessed" if feature_override is not None else "physio+pupil delta; audio absolute"
    rows_all = []
    for target, label in [("vad_valence", "Valence (high/low)"), ("vad_arousal", "Arousal (high/low)")]:
        if target not in pt.columns:
            continue
        sub = pt.dropna(subset=[target, "group_id"]).copy()
        scores, _ = logo_classifier(sub, features, target, binarize_median=True)
        scores.insert(0, "benchmark_id", "B1_affective_binary")
        scores.insert(1, "target", label)
        scores.insert(2, "n_rows", len(sub))
        scores.insert(3, "n_groups", sub["group_id"].nunique())
        scores.insert(4, "feature_set", feat_label)
        rows_all.append(scores)
    return pd.concat(rows_all, ignore_index=True) if rows_all else pd.DataFrame()


def b2_dominance_binary(pt: pd.DataFrame,
                         feature_override: list[str] | None = None) -> pd.DataFrame:
    """B2: Dominance binary classification (above/below fold-local median split)."""
    target_raw = "vad_dominance"
    if target_raw not in pt.columns:
        LOGGER.warning("B2: dominance column not found, skipping.")
        return pd.DataFrame()
    features = feature_override if feature_override is not None else available_columns(pt, PHYSIO_PUPIL_DELTA + AUDIO_ABS_FEATURES)
    feat_label = "preprocessed" if feature_override is not None else "physio+pupil delta; audio absolute"
    sub = pt[pt["task"].isin(("T0", "T1", "T2", "T3"))].dropna(subset=[target_raw, "group_id"]).copy()
    # Pass raw continuous target; logo_classifier will binarize fold-locally
    # to avoid leakage from global median computation.
    scores, _ = logo_classifier(sub, features, target_raw, binarize_median=True)
    scores.insert(0, "benchmark_id", "B2_dominance_binary")
    scores.insert(1, "target", "Dominance (high/low)")
    scores.insert(2, "n_rows", len(sub))
    scores.insert(3, "n_groups", sub["group_id"].nunique())
    scores.insert(4, "feature_set", feat_label)
    return scores

def b3_cognitive_state(pt: pd.DataFrame,
                        feature_override: list[str] | None = None) -> pd.DataFrame:
    """B3: Cognitive state prediction -- engagement + mental demand (post-block)."""
    features = feature_override if feature_override is not None else available_columns(pt, PHYSIO_PUPIL_DELTA + AUDIO_ABS_FEATURES)
    feat_label = "preprocessed" if feature_override is not None else "physio+pupil delta; audio absolute"
    rows_all = []
    for target, label in [("ans_engagement", "Engagement"), ("ans_mental_demand", "Mental demand")]:
        if target not in pt.columns:
            continue
        sub = pt.dropna(subset=[target, "group_id"]).copy()
        if len(sub) < 20:
            continue
        # Pass raw continuous target; logo_classifier binarizes fold-locally
        # to avoid leakage from global median computation.
        scores, _ = logo_classifier(sub, features, target, binarize_median=True)
        scores.insert(0, "benchmark_id", "B3_cognitive_state")
        scores.insert(1, "target", f"{label} >= median")
        scores.insert(2, "n_rows", len(sub))
        scores.insert(3, "n_groups", sub["group_id"].nunique())
        scores.insert(4, "feature_set", feat_label)
        rows_all.append(scores)
    return pd.concat(rows_all, ignore_index=True) if rows_all else pd.DataFrame()

def b4_personality_challenge(pt: pd.DataFrame, participants: pd.DataFrame,
                              feature_override: list[str] | None = None) -> pd.DataFrame:
    """B4: Personality trait classification -- challenge benchmark (n=40 participants).

    High/low BFI Extraversion and Openness via fold-local median split.
    Each test fold has only ~4 participants; AUC is near-chance.
    """
    participant_df = build_participant_table(pt, participants, feature_override=feature_override)
    feat_label = "preprocessed (CHALLENGE: n=40)" if feature_override is not None else "participant-mean physio+pupil+audio (CHALLENGE: n=40)"
    if feature_override is not None:
        features = [c for c in feature_override if c in participant_df.columns]
    else:
        features = available_columns(participant_df, PHYSIO_PUPIL_DELTA + AUDIO_ABS_FEATURES)
    rows_all = []
    for trait, label in [("bfi44_e", "Extraversion (high/low)"), ("bfi44_o", "Openness (high/low)")]:
        if trait not in participant_df.columns:
            continue
        sub = participant_df.dropna(subset=[trait, "group_id"]).copy()
        if len(sub) < 10:
            continue
        scores, _ = logo_classifier(sub, features, trait, binarize_median=True)
