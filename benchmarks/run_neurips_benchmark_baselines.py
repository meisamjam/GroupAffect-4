def _fmt(v: float, decimals: int = 3) -> str:
    return "--" if math.isnan(v) else f"{v:.{decimals}f}"


def _pick(summary: pd.DataFrame, bid: str, metric: str, col: str) -> float:
    row = summary[(summary["benchmark_id"] == bid) & (summary["metric"] == metric)]
    return float(row[col].iloc[0]) if not row.empty else math.nan


