def run_benchmarks(
    pt: pd.DataFrame,
    audio: pd.DataFrame,
    participants: pd.DataFrame,
    feature_override: list[str] | None = None,
) -> pd.DataFrame:
    """Run all benchmarks and return concatenated summary DataFrame."""
    summaries = [
        b_task_classification(pt, feature_override=feature_override),
        b1_affective_regression(pt, feature_override=feature_override),
        b2_dominance_binary(pt, feature_override=feature_override),
        b3_cognitive_state(pt, feature_override=feature_override),
        b4_personality_challenge(pt, participants, feature_override=feature_override),
        b5_meeting_performance(pt, feature_override=feature_override),
        b_speaking_balance(audio, participants, pt=pt),  # cardiac+BFI enrichment
        b6_overlap_balance(audio, participants, pt=pt),  # pupil+prosody+BFI enrichment
    ]
    summaries = [s for s in summaries if not s.empty]
    if not summaries:
        return pd.DataFrame()
    out = pd.concat(summaries, ignore_index=True)
    out["cv_protocol"] = "leave-one-group-out (split=group_id); fold-local KNN imputation"
    out["baseline_note"] = (
        "majority class (classifier) or train-fold mean (regressor); "
        "linear models use KNN imputation fit on each training fold only"
    )
    return out


