def load_tsv(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(f"Missing input: {path}")
    return pd.read_csv(path, sep="\t")


def available_columns(df: pd.DataFrame, requested: Iterable[str]) -> list[str]:
    return [c for c in requested if c in df.columns]


def gini(values: pd.Series) -> float:
    clean = values.dropna().to_numpy(dtype=float)
    if clean.size == 0 or np.isclose(clean.mean(), 0.0):
        return math.nan
    diffs = np.abs(clean[:, None] - clean[None, :]).sum()
    return float(diffs / (2 * clean.size ** 2 * clean.mean()))


def _feature_blocks(features: list[str]) -> list[list[str]]:
    """Group feature columns into rough modality blocks for fold-local KNN."""
    blocks: dict[str, list[str]] = {"physio": [], "et": [], "audio": [], "other": []}
    for col in features:
        if col.startswith("audio_"):
            blocks["audio"].append(col)
        elif col.startswith(("pupil_", "gaze_")):
            blocks["et"].append(col)
        elif col.startswith((
            "hr_",
            "hrv_",
            "eda_",
            "temp_",
            "thermopile_",
            "accel_",
            "motion_",
            "biomarker_",
        )):
            blocks["physio"].append(col)
        else:
            blocks["other"].append(col)
    return [cols for cols in blocks.values() if cols]


def _numeric_block(df: pd.DataFrame, columns: list[str]) -> pd.DataFrame:
    """Return a numeric feature block, coercing unexpected strings to NaN."""
    return df[columns].apply(pd.to_numeric, errors="coerce")


def fold_local_knn_impute(
    train: pd.DataFrame,
    test: pd.DataFrame,
    features: list[str],
) -> tuple[pd.DataFrame, pd.DataFrame]:
    """Fit KNN imputers on train folds only, per modality block.

    Columns that are entirely missing in a training fold are set to zero before
    imputation. For robust-z-scored features, zero corresponds to the person's
    typical value; for raw-feature challenge baselines it is a conservative
    train-only fallback that avoids using held-out distribution information.
    """
    from sklearn.impute import KNNImputer

    train_out = pd.DataFrame(index=train.index)
    test_out = pd.DataFrame(index=test.index)
    n_neighbors = max(1, min(KNN_K, len(train)))

    for block_cols in _feature_blocks(features):
        train_block = _numeric_block(train, block_cols).copy()
        test_block = _numeric_block(test, block_cols).copy()

        empty_train_cols = train_block.columns[train_block.isna().all()].tolist()
        if empty_train_cols:
            LOGGER.debug(
                "Fold-local KNN: %d all-missing train columns set to 0: %s",
                len(empty_train_cols),
                empty_train_cols[:5],
            )
            train_block.loc[:, empty_train_cols] = 0.0
            test_block.loc[:, empty_train_cols] = 0.0

        if train_block.isna().any().any() or test_block.isna().any().any():
            imputer = KNNImputer(n_neighbors=n_neighbors, weights="distance")
            train_values = imputer.fit_transform(train_block)
            test_values = imputer.transform(test_block)
        else:
            train_values = train_block.to_numpy(dtype=float)
            test_values = test_block.to_numpy(dtype=float)

        train_out[block_cols] = train_values
        test_out[block_cols] = test_values

    return train_out[features], test_out[features]


# â”€â”€â”€ LOGO-CV engines â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

def logo_classifier(
    df: pd.DataFrame,
    features: list[str],
    target: str,
    binarize_median: bool = False,
) -> tuple[pd.DataFrame, pd.DataFrame]:
    """LOGO-CV classifier.

    Parameters
    ----------
    binarize_median:
        If True, ``target`` is a raw continuous column.  The binary split
        threshold is computed as the **training-fold median** inside each LOGO
        fold and applied to both train and test.  This avoids the leakage that
        arises from computing a global median before the CV loop (which allows
        test-fold values to influence the threshold used to label those same
        test rows).
    """
    from sklearn.linear_model import LogisticRegression
    from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
    from sklearn.pipeline import make_pipeline
    from sklearn.preprocessing import StandardScaler

    groups = sorted(df["group_id"].dropna().unique())
    # When binarize_median=False the target must already be binary;
    # when True the unique-count check runs after fold-local binarization.
    is_binary = True if binarize_median else df[target].nunique() == 2
    predictions: list[pd.DataFrame] = []

    for fold, gid in enumerate(groups, 1):
        train = df[df["group_id"] != gid].copy()
        test = df[df["group_id"] == gid].copy()

        if binarize_median:
            # Compute threshold on training fold only â€” no test data involved
            fold_threshold = float(train[target].dropna().median())
            bin_col = f"{target}_binary"
            train[bin_col] = (train[target] >= fold_threshold).astype(int)
            test[bin_col] = (test[target] >= fold_threshold).astype(int)
            active_target = bin_col
        else:
            active_target = target

        majority = train[active_target].mode(dropna=True).iloc[0]
        base_pred = pd.Series([majority] * len(test), index=test.index)

        if train[active_target].nunique() < 2:
            model_pred = base_pred.copy()
            model_score = np.full(len(test), np.nan)
        else:
            x_train, x_test = fold_local_knn_impute(train, test, features)
            model = make_pipeline(
                StandardScaler(),
                LogisticRegression(max_iter=2000, class_weight="balanced"),
            )
            model.fit(x_train, train[active_target])
            model_pred = pd.Series(model.predict(x_test), index=test.index)
            model_score = (
                model.predict_proba(x_test)[:, 1]
                if is_binary and len(getattr(model[-1], "classes_", [])) == 2
                else np.full(len(test), np.nan)
            )

        fp = test[["group_id", "task"]].copy() if "task" in test.columns else test[["group_id"]].copy()
        fp[active_target] = test[active_target].values
        fp["fold"] = fold
        fp["baseline_prediction"] = base_pred.values
        fp["model_prediction"] = model_pred.values
        fp["model_score"] = model_score
        predictions.append(fp)

    pred = pd.concat(predictions, ignore_index=True)
    active_target = f"{target}_binary" if binarize_median else target
    y_true = pred[active_target]
    rows = [
        {"metric": "accuracy",
         "baseline": accuracy_score(y_true, pred["baseline_prediction"]),
         "linear_model": accuracy_score(y_true, pred["model_prediction"])},
        {"metric": "macro_f1",
         "baseline": f1_score(y_true, pred["baseline_prediction"], average="macro", zero_division=0),
         "linear_model": f1_score(y_true, pred["model_prediction"], average="macro", zero_division=0)},
    ]
    if is_binary:
        try:
            auc = roc_auc_score(y_true.astype(int), pred["model_score"])
        except ValueError:
            auc = math.nan
        rows.append({"metric": "roc_auc", "baseline": math.nan, "linear_model": auc})

    return pd.DataFrame(rows), pred


def logo_regressor(
    df: pd.DataFrame,
    features: list[str],
    target: str,
) -> tuple[pd.DataFrame, pd.DataFrame]:
    from scipy.stats import pearsonr
    from sklearn.linear_model import Ridge
    from sklearn.metrics import mean_absolute_error, mean_squared_error
    from sklearn.pipeline import make_pipeline
    from sklearn.preprocessing import StandardScaler

    groups = sorted(df["group_id"].dropna().unique())
    predictions: list[pd.DataFrame] = []

    for fold, gid in enumerate(groups, 1):
        train = df[df["group_id"] != gid]
        test = df[df["group_id"] == gid]
        baseline_val = float(train[target].mean())
        train_clean = train.dropna(subset=[target])
        if len(train_clean) < 3:
            preds = np.full(len(test), baseline_val)
        else:
            x_train, x_test = fold_local_knn_impute(train_clean, test, features)
            model = make_pipeline(StandardScaler(), Ridge(alpha=10.0))
            model.fit(x_train, train_clean[target])
            preds = model.predict(x_test)

        fp = test[["group_id"]].copy()
        if "task" in test.columns:
            fp["task"] = test["task"].values
        fp[target] = test[target].values
        fp["fold"] = fold
        fp["baseline_prediction"] = baseline_val
        fp["model_prediction"] = preds
        predictions.append(fp)

    pred = pd.concat(predictions, ignore_index=True)
    mask = pred[target].notna()
    y_true = pred.loc[mask, target].to_numpy(dtype=float)
    y_base = pred.loc[mask, "baseline_prediction"].to_numpy(dtype=float)
    y_model = pred.loc[mask, "model_prediction"].to_numpy(dtype=float)

    try:
        model_r = pearsonr(y_true, y_model).statistic
    except ValueError:
        model_r = math.nan

    rows = [
        {"metric": "rmse",
         "baseline": mean_squared_error(y_true, y_base) ** 0.5,
         "linear_model": mean_squared_error(y_true, y_model) ** 0.5},
        {"metric": "mae",
         "baseline": mean_absolute_error(y_true, y_base),
         "linear_model": mean_absolute_error(y_true, y_model)},
        {"metric": "pearson_r", "baseline": math.nan, "linear_model": model_r},
    ]
    return pd.DataFrame(rows), pred


