def _pt_group_task_means(
    pt: pd.DataFrame,
    feat_cols: list[str],
    active_tasks: tuple[str, ...] = ACTIVE_TASKS,
) -> pd.DataFrame:
    """Aggregate participant-task pt to group-task means for selected features."""
    cols = [c for c in feat_cols if c in pt.columns]
    if not cols or "group_id" not in pt.columns or "task" not in pt.columns:
        return pd.DataFrame()
    sub = pt[pt["task"].isin(active_tasks)][["group_id", "task"] + cols].copy()
    return sub.groupby(["group_id", "task"], as_index=False)[cols].mean()


def b_speaking_balance(
    audio: pd.DataFrame,
    participants: pd.DataFrame,
    pt: pd.DataFrame | None = None,
) -> pd.DataFrame:
    """B_speaking: Group-task speaking-balance Gini from cardiac physio + BFI + task indicators.

    Cardiac and EDA group-means are merged when ``pt`` is provided; ablation
    results show that cardiac features drive most of the (small) speaking-Gini
    signal, outperforming BFI-only and pushing MAE below the mean baseline.
    """
    final_groups = set(participants["group_id"].unique())
    audio2 = audio.copy().reset_index(drop=True)
    if "task_id" in audio2.columns:
        if "task" in audio2.columns:
            audio2 = audio2.drop(columns=["task"])
        audio2 = audio2.rename(columns={"task_id": "task"})
    grp_ok = audio2["group_id"].isin(final_groups).to_numpy() if "group_id" in audio2.columns else np.ones(len(audio2), bool)
    task_ok = audio2["task"].isin(ACTIVE_TASKS).to_numpy()
    avail_ok = audio2["audio_available"].astype(bool).to_numpy() if "audio_available" in audio2.columns else np.ones(len(audio2), bool)
    sub = audio2[grp_ok & task_ok & avail_ok].copy()
    balance = (
        sub.groupby(["group_id", "task"], as_index=False)
        .agg(gini_speaking=("speaking_fraction", gini))
        .dropna(subset=["gini_speaking"])
    )
    bfi = participants.groupby("group_id", as_index=False)[BFI_FEATURES].mean()
    design = balance.merge(bfi, on="group_id", how="left")
    task_dummies = pd.get_dummies(design["task"], prefix="task", dtype=float)
    design = pd.concat([design, task_dummies], axis=1)

    # Enrich with group-task cardiac physio means when pt is available
    extra_feats: list[str] = []
    if pt is not None:
        group_cardiac = _pt_group_task_means(pt, CARDIAC_GROUP_COLS)
        if not group_cardiac.empty:
            cardiac_cols = [c for c in CARDIAC_GROUP_COLS if c in group_cardiac.columns]
            design = design.merge(group_cardiac, on=["group_id", "task"], how="left")
            extra_feats = cardiac_cols

    feat_label = ("group cardiac+EDA physio means + BFI means + task indicators"
                  if extra_feats else "group BFI means + task indicators")
    features = extra_feats + BFI_FEATURES + task_dummies.columns.tolist()
    features = [f for f in features if f in design.columns]
    scores, _ = logo_regressor(design, features, "gini_speaking")
    scores.insert(0, "benchmark_id", "B6_speaking_balance")
    scores.insert(1, "target", "Speaking-fraction Gini (group-task)")
    scores.insert(2, "n_rows", len(design))
    scores.insert(3, "n_groups", design["group_id"].nunique())
    scores.insert(4, "feature_set", feat_label)
    return scores


def b6_overlap_balance(
    audio: pd.DataFrame,
    participants: pd.DataFrame,
    pt: pd.DataFrame | None = None,
) -> pd.DataFrame:
    """B6: Group-task speech-overlap balance from prosody + BFI + task indicators.

    Target: group-mean VAD-derived overlap fraction per group-task block.
    Inputs: group-mean prosodic features (energy, pitch SD, HNR,
    speech-rate proxy), group-mean BFI trait scores, and task
    one-hot indicators.  Overlap fraction itself is the target and is excluded
    from the feature set.  speaking_fraction is also excluded because it is
    derived from the same per-channel VAD computation as overlap_fraction,
    making it a leakage source.  Protocol mirrors B_speaking_balance: LOGO-CV,
    Ridge regression, MAE + Pearson r.
    """
    final_groups = set(participants["group_id"].unique())
    audio2 = audio.copy().reset_index(drop=True)
    if "task_id" in audio2.columns:
        if "task" in audio2.columns:
            audio2 = audio2.drop(columns=["task"])
        audio2 = audio2.rename(columns={"task_id": "task"})
    grp_ok = audio2["group_id"].isin(final_groups).to_numpy() if "group_id" in audio2.columns else np.ones(len(audio2), bool)
    task_ok = audio2["task"].isin(ACTIVE_TASKS).to_numpy()
    avail_ok = audio2["audio_available"].astype(bool).to_numpy() if "audio_available" in audio2.columns else np.ones(len(audio2), bool)
    sub = audio2[grp_ok & task_ok & avail_ok].copy()

    prosodic_candidates = [
        "energy_mean", "pitch_sd", "hnr_mean", "speech_rate_proxy",
        # speaking_fraction excluded: derived from same VAD source as target
    ]
    prosodic_feats = [c for c in prosodic_candidates if c in sub.columns]
    target_col = "overlap_fraction"
    if target_col not in sub.columns:
        return pd.DataFrame()

    agg_spec = {k: (k, "mean") for k in [target_col] + prosodic_feats}
    balance = (
        sub.groupby(["group_id", "task"], as_index=False)
        .agg(**agg_spec)
        .dropna(subset=[target_col])
    )
    bfi = participants.groupby("group_id", as_index=False)[BFI_FEATURES].mean()
    design = balance.merge(bfi, on="group_id", how="left")
    task_dummies = pd.get_dummies(design["task"], prefix="task", dtype=float)
    design = pd.concat([design, task_dummies], axis=1)
    # Enrich with group-task pupil means when pt is available.
    # Modality ablation shows pupil-only MAE ~0.057 beats prosodic-only for
    # overlap fraction; adding it to the design matrix should recover that gain.
    extra_pupil: list[str] = []
    if pt is not None:
        group_pupil = _pt_group_task_means(pt, PUPIL_GROUP_COLS)
        if not group_pupil.empty:
            pupil_cols = [c for c in PUPIL_GROUP_COLS if c in group_pupil.columns]
            design = design.merge(group_pupil, on=["group_id", "task"], how="left")
            extra_pupil = pupil_cols

    feat_label = ("group pupil means + prosody + BFI means + task indicators"
                  if extra_pupil else "group prosody + BFI means + task indicators")
    feature_cols = [f for f in extra_pupil + prosodic_feats + BFI_FEATURES + task_dummies.columns.tolist()
                    if f in design.columns]
    scores, _ = logo_regressor(design, feature_cols, target_col)
    scores.insert(0, "benchmark_id", "B7_overlap_balance")
    scores.insert(1, "target", "Overlap fraction (group-task mean)")
    scores.insert(2, "n_rows", len(design))
    scores.insert(3, "n_groups", design["group_id"].nunique())
    scores.insert(4, "feature_set", feat_label)
    return scores


