def write_comparison(raw_summary: pd.DataFrame, pre_summary: pd.DataFrame, path: Path) -> None:
    """Write a side-by-side raw vs preprocessed comparison table to TSV."""
    rows = []
    for bid in raw_summary["benchmark_id"].unique():
        for tgt in raw_summary[raw_summary["benchmark_id"] == bid]["target"].unique():
            raw_row = raw_summary[(raw_summary["benchmark_id"] == bid) & (raw_summary["target"] == tgt)]
            pre_row = pre_summary[(pre_summary["benchmark_id"] == bid) & (pre_summary["target"] == tgt)]
            for metric in ["accuracy", "roc_auc", "mae", "pearson_r"]:
                raw_v = raw_row[raw_row["metric"] == metric]["linear_model"].values
                pre_v = pre_row[pre_row["metric"] == metric]["linear_model"].values
                if len(raw_v) == 0 and len(pre_v) == 0:
                    continue
                rows.append({
                    "benchmark_id": bid,
                    "target": tgt,
                    "metric": metric,
                    "raw_linear": float(raw_v[0]) if len(raw_v) > 0 else float("nan"),
                    "preprocessed_linear": float(pre_v[0]) if len(pre_v) > 0 else float("nan"),
                })
    df = pd.DataFrame(rows)
    df["delta"] = df["preprocessed_linear"] - df["raw_linear"]
    path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(path, sep="\t", index=False)
    LOGGER.info("Wrote comparison table: %s", path)
    # Print to console
    print("\n=== RAW vs PREPROCESSED COMPARISON ===")
    for _, r in df.iterrows():
        direction = "+" if r["delta"] > 0 else ""
        print(f"  {r['benchmark_id'][:30]:30s} {r['target'][:20]:20s} {r['metric']:12s} "
              f"raw={r['raw_linear']:6.3f}  pre={r['preprocessed_linear']:6.3f}  delta={direction}{r['delta']:+.3f}")


