def merge_audio(base: pd.DataFrame, audio: pd.DataFrame) -> pd.DataFrame:
    """Add absolute audio features to a participant-task base table."""
    audio = audio.copy()
    if "task_id" in audio.columns and "task" not in audio.columns:
        audio = audio.rename(columns={"task_id": "task"})
    src_cols = [
        "speaking_fraction", "overlap_fraction", "energy_mean", "energy_sd",
        "pitch_mean", "pitch_sd", "hnr_mean", "jitter_mean", "shimmer_mean",
        "voiced_segments_per_sec", "mean_voiced_segment_s", "speech_rate_proxy",
    ]
    avail = available_columns(audio, src_cols)
    key_cols = [c for c in ["session_id", "participant_id", "task", "group_id"] if c in audio.columns]
    audio_sel = audio[key_cols + avail].rename(columns={c: f"audio_{c}" for c in avail})
    merge_on = [c for c in ["session_id", "participant_id", "task"] if c in base.columns and c in audio_sel.columns]
    return base.merge(audio_sel, on=merge_on, how="left", suffixes=("", "_audio"))


def build_pt_table(
    stats_pt: pd.DataFrame,
    audio: pd.DataFrame,
    vad: pd.DataFrame,
) -> pd.DataFrame:
    """Participant-task table: physio+pupil delta, audio absolute, VAD labels."""
    base = stats_pt.copy()
    # Normalise task column
    if "task_id" in base.columns and "task" not in base.columns:
        base = base.rename(columns={"task_id": "task"})
    base = base[base["task"].isin(ACTIVE_TASKS)].copy()

    # Merge audio
    base = merge_audio(base, audio)

    # Merge VAD labels (valence, arousal, dominance)
    vad2 = vad.rename(columns={
        "participant": "participant_id",
        "arousal": "vad_arousal",
        "valence": "vad_valence",
        "dominance": "vad_dominance",
    }).copy()
    merge_on = [c for c in ["session_id", "participant_id", "task"] if c in base.columns and c in vad2.columns]
    for col in ["vad_arousal", "vad_valence", "vad_dominance"]:
        if col in base.columns:
            base = base.drop(columns=[col])
    vad_cols = merge_on + [c for c in ["vad_arousal", "vad_valence", "vad_dominance"] if c in vad2.columns]
    base = base.merge(vad2[vad_cols], on=merge_on, how="left")
    return base


def build_participant_table(
    pt: pd.DataFrame,
    participants: pd.DataFrame,
    feature_override: list[str] | None = None,
) -> pd.DataFrame:
    """Participant-level table: mean features across T1â€“T4 + BFI labels."""
    uid_col = "participant_global_id" if "participant_global_id" in pt.columns else "participant_uid"
    if feature_override is not None:
        feat_cols = [c for c in feature_override if c in pt.columns]
    else:
        feat_cols = available_columns(pt, PHYSIO_PUPIL_DELTA + [f"audio_{c.replace('audio_','')}" for c in AUDIO_ABS_FEATURES])
    agg = (
        pt[[uid_col] + feat_cols]
        .groupby(uid_col)[feat_cols]
        .mean()
        .reset_index()
    )
    # Merge BFI labels
    parts = participants.rename(columns={"participant_id": uid_col}).copy()
    bfi_cols = [c for c in BFI_FEATURES if c in parts.columns]
    group_col = [c for c in ["group_id"] if c in parts.columns]
    merged = agg.merge(parts[[uid_col] + bfi_cols + group_col], on=uid_col, how="inner")
    return merged


